export const defineColor = (nam) => {
  switch (nam) {
    case `transmute`:
      return `#A8DADC`;
    case `aug`:
      return `#83C5BE`;
    case `regal`:
      return `#B0BEC5`;
    case `alch`:
      return `#64B5F6`;
    case `exalted`:
      return `#42A5F5`;
    case `chaos`:
      return `#FFEB3B`;
    case `annul`:
      return `#FFA726`;
    case `divine`:
      return `#EF5350`;
    case `mirror`:
      return `#D32F2F`;
    case `vaal`:
      return `#81C784`;
    case `hinekoras-lock`:
      return `#F44336`;
    case `chance`:
      return `#8E24AA`;
    case `fracturing-orb`:
      return `#FFC107`;
    default:
      return `#ffffff`;
  }
};
